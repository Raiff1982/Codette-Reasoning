    ap.add_argument("--only", default="", help="Comma-separated adapter names (default: all)")
    args = ap.parse_args()

    outdir = Path(args.outdir); outdir.mkdir(parents=True, exist_ok=True)
    names = [n.strip() for n in args.only.split(",") if n.strip()] or list(PER_ADAPTER)
    total = 0
    for name in names:
        records = build_records(name)
        out = outdir / f"{name}_reasoning.jsonl"
        with out.open("w", encoding="utf-8") as f:
            for r in records:
                f.write(json.dumps(r, ensure_ascii=False) + "\n")
        print(f"{name:22} {len(records):3} examples -> {out}")
        total += len(records)
    print(f"TOTAL: {total} examples across {len(names)} adapters")


